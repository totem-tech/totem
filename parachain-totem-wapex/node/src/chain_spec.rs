fn testnet_genesis(
	invulnerables: Vec<(AccountId, AuraId)>,
	endowed_accounts: Vec<AccountId>,
	id: ParaId,
	root_key: AccountId,
) -> parachain_totem_wapex_runtime::GenesisConfig {
	parachain_totem_wapex_runtime::GenesisConfig {
		system: parachain_totem_wapex_runtime::SystemConfig {
			code: parachain_totem_wapex_runtime::WASM_BINARY
				.expect("WASM binary was not build, please build it!")
				.to_vec(),
			// changes_trie_config: Default::default(),
		},
		balances: parachain_totem_wapex_runtime::BalancesConfig {
			balances: endowed_accounts.iter().cloned().map(|k| (k, 1 << 60)).collect(),
		},
		parachain_info: parachain_totem_wapex_runtime::ParachainInfoConfig { parachain_id: id },
		collator_selection: parachain_totem_wapex_runtime::CollatorSelectionConfig {
			invulnerables: invulnerables.iter().cloned().map(|(acc, _)| acc).collect(),
			candidacy_bond: EXISTENTIAL_DEPOSIT * 1_600_000,
			..Default::default()
		},
		session: parachain_totem_wapex_runtime::SessionConfig {
			keys: invulnerables
				.into_iter()
				// .cloned()
				.map(|(acc, aura)| {
					(
						acc.clone(),                 // account id
						acc,                         // validator id
						kapex_session_keys(aura),    // session keys
					)
				})
				.collect(),
		},
		// no need to pass anything to aura, in fact it will panic if we do. Session will take care
		// of this.
		aura: Default::default(),
		aura_ext: Default::default(),
		parachain_system: Default::default(),
		sudo: parachain_totem_wapex_runtime::SudoConfig { key: root_key },
		// funding: FundingConfig::default(),
	}
}
